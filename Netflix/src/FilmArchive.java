    public void filmView()  {
        model.setRowCount(0);
        ArrayList<Film> films=new ArrayList<Film>();
        films = op.FilmCome();
        
        if(films !=null){
            for(Film filmx : films){//filmsin içindekini filmxe aktarıyorum
                Object[] add={
                    filmx.getId(),filmx.getAdi(),filmx.getTipi(),filmx.getTuru()
                };
                
            model.addRow(add);
            }
        }
    }   
    
    
